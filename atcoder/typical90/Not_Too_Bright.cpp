    cin >> h >> w;

    if (h == 1 || w == 1) {
        cout << h * w << endl;
        return 0;
    }

    int w_rem, num_col;
    w_rem = w % 2;
    if (w_rem == 0) num_col = w / 2;
    else num_col = (w / 2) + 1;

    int h_rem, num_row;
    h_rem = h % 2;
    if (h_rem == 0) num_row = h / 2;
