int main() {

    int n, s;
    cin >> n >> s;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; i ++) cin >> a.at(i);

    vector<vector<int>> dp(n + 1, vector<int>(s + 1));
    dp[0][0] = 1;
    for (int i = 1; i <= s; i++) dp[0][i] = 0;

    for (int i = 1; i <= n; i ++) {
        for (int j = 0; j <= s; j ++) {
            if (j < a[i]) dp[i][j] = dp[i - 1][j];
            if (j >= a[i]) dp[i][j] = max(dp[i - 1][j], dp[i - 1][j - a[i]]);
        }
    }

    if (dp[n][s]) cout << "Yes" << endl;
    else cout << "No" << endl;

    return 0;
}